//6) Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a 
//área do retângulo. 
function executar6(){
    //Entrada de dados
    let base = parseFloat(prompt("Digite a base do retângulo: "))
    let altura = parseFloat(prompt("Digite a altura do retângulo: "))

    //Processamento
    let area = base * altura

    //Saída de dados
    alert(`A base desse retângulo é: ${area}`)
}

